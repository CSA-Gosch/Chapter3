package MetricConversion;

public class GetMeasurments {
    private double gallonConvert;
    private double inchesConvert;
    private double centimeterConvert;
    private double literConvert;

    public void setGallonConvert(double gallon) {
        gallonConvert = gallon;
    }

    public double getGallonConvert() {
        return gallonConvert;
    }

    public void setInchesConvert(double inches) {
        inchesConvert = inches;
    }

    public double getInchesConvert() {
        return inchesConvert;
    }

    public void setCentimeterConvert(double centimeter) {
        centimeterConvert = centimeter;
    }

    public double getCentimeterConvert() {
        return centimeterConvert;
    }

    public void setLiterConvert(double liter) {
        literConvert = liter;
    }

    public double getLiterConvert() {
        return literConvert;
    }
}

