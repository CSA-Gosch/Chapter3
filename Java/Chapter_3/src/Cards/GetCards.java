package Cards;

public class GetCards {
    private char suitBruh;
    private int cardBruh;

    public void setCardBruh(int card) {
        cardBruh = card;
    }

    public int getCardBruh() {
        return cardBruh;
    }

    public void setSuitBruh(char suit) {
        suitBruh = suit;
    }

    public char getSuitBruh() {
        return suitBruh;
    }
}
